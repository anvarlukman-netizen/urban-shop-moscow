async function seed() {
  const rows = await db.select().from(products);
  if (rows.length > 0) {
    console.log(`DB already has ${rows.length} products, skipping seed`);
    process.exit(0);
  }
  await db.insert(products).values(seedData as any[]);
  console.log(`Seeded ${seedData.length} products`);
  process.exit(0);
}

seed().catch((err) => {
  console.error(err);
  process.exit(1);
});
